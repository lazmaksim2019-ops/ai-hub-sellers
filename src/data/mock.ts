export function getRandomTags(count: number): string[] {
  const shuffled = [...TAGS].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, TAGS.length));
}

export function getRandomPrice(marketplace: Marketplace): number {
  const range = PRICE_RANGES[marketplace];
  const price = range.min + Math.random() * (range.max - range.min);
  return Math.round(price / 10) * 10;
}

export function getRandomReply(sentiment: Sentiment): string {
  const replies = REVIEW_REPLIES[sentiment];
  return replies[Math.floor(Math.random() * replies.length)];
}

export function getRandomDescription(marketplace: Marketplace): string {
  const descriptions = SEO_DESCRIPTIONS[marketplace];
  return descriptions[Math.floor(Math.random() * descriptions.length)];
}
